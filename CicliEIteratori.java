class CicliEIteratori {
  public static void main(String[] args){
      // dichiaro variabile
      int contatore = 1;

      // while
      while (contatore < 10) {
          System.out.println("contatore: " + contatore);
          contatore++;
      } // end while


      int contatoreDoWhile = 1;
        do {
            System.out.println("contatoreDoWhile: " + contatoreDoWhile);
            contatoreDoWhile++;
        } while (contatoreDoWhile < 5);


  }
}